package moe.kawayi.org.utopia.desktop.graphics.yongle;

public class Rect {
    private int height;

    private int width;

    public Rect(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
